a = 5
b = 2

# Addition Operator is ==> +
print("Sum is",a+b)

# Subtraction Operaor is ==> -
print("Difference is",a-b)

# Multiplication Operator is ==> *
print("Multiplication is",a*b)

# Division Operator is ==> /
print("Division is",a/b) # division result always be of float type

# Modulus Operator to find out reminder is ==> %
print("Reminder is",a%b)

# Power Operator is ==> **
print("Power Operation Result is",a**b)